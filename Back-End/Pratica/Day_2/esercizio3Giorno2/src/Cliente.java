
public class Cliente {

	int codiceCliente;
	String nome;
	String cognome;
	String email;
	String data;
	
	public Cliente(int codiceCliente, String nome, String cognome, String email, String data) {
		this.codiceCliente = codiceCliente;
		this.nome = nome;
		this.cognome = cognome;
		this.email = email;
		this.data = data;
	}
}
