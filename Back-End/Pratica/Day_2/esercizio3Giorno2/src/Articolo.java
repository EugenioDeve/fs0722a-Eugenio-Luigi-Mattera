
public class Articolo {

	int codiceArticolo;
	String descrizione;
	double prezzo;
	int stock;
	
	public Articolo( int codiceArticolo, String descrizione, double prezzo, int stock) {
		this.codiceArticolo = codiceArticolo;
		this.descrizione = descrizione;
		this.prezzo = prezzo;
		this.stock = stock;
	}
	
}
